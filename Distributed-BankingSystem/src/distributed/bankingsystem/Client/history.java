    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String m1=jComboBox1.getSelectedItem().toString();
            String y1=jComboBox2.getSelectedItem().toString();
            String m2=jComboBox3.getSelectedItem().toString();
            String y2=jComboBox4.getSelectedItem().toString();
            
            dos.writeUTF(AESencrp.encrypt("7,"+ID+","+y1+","+m1+","+y2+","+m2));
            String reply =  AESencrp.decrypt(dis.readUTF());
            String[] parsedReply = reply.split(";");
            System.out.println(parsedReply[0]);
            if(parsedReply[0].equals(",,")) jLabel8.setText("No History Available");
            else
            {
                for (int i=0;i<parsedReply.length;i++)
                {
                    String[] parsedparsedReply = parsedReply[i].split(",");
                    Object[] row = { parsedparsedReply[0], parsedparsedReply[1], parsedparsedReply[2]};
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.addRow(row);
                }
            }
        }
        catch (IOException ex) {
            Logger.getLogger(history.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(history.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

